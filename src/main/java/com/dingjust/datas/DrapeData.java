public class DrapeData {
    String view;
    String drapeName;
    String fabricName;
    int sort;

    public String getView() {
        return view;
    }

    public void setView(String view) {
        this.view = view;
    }

    public String getDrapeName() {
        return drapeName;
    }

    public void setDrapeName(String drapeName) {
        this.drapeName = drapeName;
    }

    public String getFabricName() {
        return fabricName;
    }

    public void setFabricName(String fabricName) {
        this.fabricName = fabricName;
    }

    public int getSort() {
        return sort;
    }

    public void setSort(int sort) {
        this.sort = sort;
    }
}
